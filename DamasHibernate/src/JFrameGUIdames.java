    //Comrova si la casella es X
    public boolean esX (int fila, int columna){
        if (jTable2.getValueAt(fila, columna) == "X"){
            return true;
        } else {
            return false;
        }
    }
    
    //Comprova si la casella es O
    public boolean esO (int fila, int columna){
        if (jTable2.getValueAt(fila, columna) == "O"){
            return true;
        } else {
            return false;
        }
     }
    
    //Obte la fila
    public int obtenirFilaClicada (){
        int fila = jTable2.getSelectedRow();
        
        return fila;
    }
    
    //Obte la columna
    public int obtenirColumnaClicada (){
        int columna = jTable2.getSelectedColumn();
        
        return columna;
    }
    
    public boolean noHiHaOrigen(){
        if(filaOrigen == -1 && columnaOrigen == -1){
            return true;
        } else {
            return false;
        }
    }
    
    public void actualitzaNouOrigen(int fila, int columna){
        filaOrigen = obtenirFilaClicada();
        columnaOrigen = obtenirColumnaClicada();
                
    }
            
    public void mostraError(){
        System.out.println("Error");
    }
    
    public boolean movimentValid(int fila, int columna){
        return true;
    }
    
    //Comprova si hi ha una casella buida, retorna true si esta buida retorna false si esta plena
    public boolean esBuit(int fila, int columna){
        if(jTable2.getValueAt(fila, columna) == null){
            return true;
        } else {
            return false;
        }        
    }
    
    //Comprova si la casella esta ocupada per ficha contraria
    public boolean ocupatContrari(int fila, int columna){
        if(jugaX && jTable2.getValueAt(fila, columna) == "O"){
            return true;
        } else if (jugaO && jTable2.getValueAt(fila, columna) == "X"){
            return true;
        } else {
            return false;
        }
    }
    
    public void mou(int fila, int columna){
        if(jugaO){
            jTable2.setValueAt("X", fila, columna);
            
        } else if (jugaX){
            jTable2.setValueAt("O", fila, columna);
        }
        
    }
    
    //Comprova si la casella esta ocupada per una ficha igual a la seva
    public boolean ocupatPropi(int fila, int columna){
        if(jugaX && jTable2.getValueAt(fila, columna) == "X"){
            return true;
        } else if (jugaO && jTable2.getValueAt(fila, columna) == "O"){
            return true;
        } else {
            return false;
        }
    }
            
    public void mostraErrorMoviment(){
        System.out.println("Error de moviment");
    }
        
